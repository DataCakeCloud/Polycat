package io.polycat.catalog.store.mapper;

import org.apache.ibatis.annotations.Param;
import org.apache.ibatis.annotations.Select;
import org.apache.ibatis.annotations.Update;
import org.springframework.stereotype.Component;

import java.util.List;

@Component
public interface SchemaMapper {
    void createSchema(@Param("projectId") String projectId);

    @Select("SELECT 1 FROM information_schema.schemata WHERE schema_name = 'schema_${projectId}'")
    List<Integer> doesExistSchema(@Param("projectId") String projectId);

    @Update("DROP SCHEMA schema_${projectId} CASCADE")
    void dropSchema(@Param("projectId") String projectId);
}
